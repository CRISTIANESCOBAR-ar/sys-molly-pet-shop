// ─── Importación en batches ───────────────────────────────────────────────────
async function importar() {
  console.log(`\nImportando ${VENTAS_RAW.length} ventas históricas...`)

  const BATCH_SIZE = 499
  let batch = db.batch()
  let batchCount = 0
  let total = 0

  for (const [fecha, producto, precio, pago] of VENTAS_RAW) {
    const metodo_pago = normalizarPago(pago)
    const fechaDate = parseDate(fecha)

    const docRef = db.collection('ventas').doc()
    batch.set(docRef, {
      fecha: admin.firestore.Timestamp.fromDate(fechaDate),
      items: [{ nombre: producto.trim().toUpperCase(), qty: 1, precio }],
      subtotal: precio,
      recargo_metodo: 0,
      total: precio,
      metodo_pago,
      id_usuario: null,
      origen: 'historial',
      creado_en: admin.firestore.FieldValue.serverTimestamp(),
    })

    total++
    batchCount++

    if (batchCount === BATCH_SIZE) {
      await batch.commit()
      console.log(`  ✓ ${total} ventas procesadas...`)
      batch = db.batch()
      batchCount = 0
    }
  }

  if (batchCount > 0) {
    await batch.commit()
  }

  console.log(`\n✅ Importación completa: ${total} ventas guardadas en Firestore.`)
  console.log('   (9 filas omitidas — ver comentarios al inicio del script)\n')
  process.exit(0)
}

importar().catch((err) => {
  console.error('❌ Error durante la importación:', err)
  process.exit(1)
})
