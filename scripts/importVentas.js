// ─── Normalización de método de pago ─────────────────────────────────────────
function normalizarPago(raw) {
  if (!raw) return 'efectivo'
  const s = raw.trim().toUpperCase().replace(/[-\s]+/g, ' ').trim()
  if (
    s === 'TRANSFERENCIA' ||
    s === 'TRNAFERENCIA' ||
    s === 'TRANSFRERENCIA' ||
    s === 'TRANSFRENCIA' ||
    s === 'TRANSFENCIA' ||
    s.startsWith('TRANSF')
  )
    return 'transferencia'
  if (s === 'DEBITO') return 'debito'
  if (s === 'TARJETA' || s === 'CREDITO') return 'credito'
  return 'efectivo'
}

// ─── Parseo de fecha DD/MM/YY (tolera barras dobles como 21/04//26) ──────────
function parseDate(str) {
  const clean = str.replace(/\/+/g, '/')
  const [d, m, y] = clean.split('/')
  return new Date(2000 + parseInt(y), parseInt(m) - 1, parseInt(d), 12, 0, 0)
}

